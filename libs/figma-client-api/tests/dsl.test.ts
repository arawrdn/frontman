describe("Verbose expansion output", () => {
  it("should expand homogeneous children instead of collapsing", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "stars",
      s: 100,
      t: 6000,
      c: Array.from({ length: 3 }, (_, i) => ({
        i: `0:${i + 2}`,
        n: "container",
        s: 100,
        t: 100,
      })),
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toContain("stars(v:6) #0:1:");
    expect(result).toContain("container #0:2");
    expect(result).toContain("container #0:3");
    expect(result).toContain("container #0:4");
  });

  it("should keep generic wrappers and single children", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "card",
      s: 100,
      t: 500,
      c: [
        {
          i: "0:2",
          n: "container",
          s: 50,
          t: 200,
          c: [{ i: "0:3", n: "title", s: 50, t: 50 }],
        },
      ],
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toContain("card(v:3) #0:1:");
    expect(result).toContain("container(v:2) #0:2:");
    expect(result).toContain("title #0:3");
  });

  it("should expand instance internals", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "page",
      s: 100,
      t: 10000,
      c: [
        {
          i: "I0:1;123:456",
          n: "component-instance",
          s: 500,
          t: 5000,
          c: [
            {
              i: "I0:1;123:456;789:012",
              n: "deep-nested",
              s: 200,
              t: 2000,
              c: [{ i: "I0:1;123:456;789:012;111:222", n: "deeper", s: 100, t: 100 }],
            },
          ],
        },
      ],
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toContain("component-instance(v:6) #I0:1;123:456:");
    expect(result).toContain("deep-nested(v:5) #I0:1;123:456;789:012:");
    expect(result).toContain("deeper #I0:1;123:456;789:012;111:222");
  });

  it("should expand decorative children even at high volume", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "pattern-bg",
      s: 100,
      t: 15000, // v:7
      c: [
        { i: "0:2", n: "path", s: 100, t: 200 },
        { i: "0:3", n: "path", s: 100, t: 200 },
      ],
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toContain("pattern-bg(v:7) #0:1:");
    expect(result).toContain("path #0:2");
    expect(result).toContain("path #0:3");
  });

  it("should keep icon names verbatim", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "vuesax-linear-check-circle",
      s: 100,
      t: 500,
      c: [{ i: "0:2", n: "vector", s: 50, t: 50 }],
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toContain("vuesax-linear-check-circle(v:3) #0:1:");
    // "vector" matches auto-generated pattern, so it's normalized to "container"
    expect(result).toContain("container #0:2");
  });
});

