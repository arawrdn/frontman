describe("skeletonToDSL", () => {
  it("should convert simple leaf node", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "title",
      s: 50,
      t: 50,
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toBe("title #0:1");
  });

  it("should convert price node", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "$23",
      s: 50,
      t: 50,
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toBe('price #0:1: "$23"');
  });

  it("should convert node with children", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "card",
      s: 100,
      t: 500,
      c: [
        { i: "0:2", n: "title", s: 50, t: 50 },
        { i: "0:3", n: "subtitle", s: 50, t: 50 },
      ],
    };
    const result = skeletonToDSL(skeleton);
    expect(result).toContain("card(v:3) #0:1:");
    expect(result).toContain("title #0:2");
    expect(result).toContain("subtitle #0:3");
  });

  it("should add source file reference", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:1",
      n: "page",
      s: 100,
      t: 100,
    };
    const result = skeletonToDSL(skeleton, { sourceFile: "design.figma.json" });
    expect(result).toContain("@source: design.figma.json");
  });
});

describe("expandedSkeletonToDSL", () => {
  it("should convert expanded skeleton format", () => {
    const skeleton: NodeSkeleton = {
      id: "0:1",
      name: "header",
      selfTokenCount: 50,
      totalTokenCount: 150,
      children: [
        { id: "0:2", name: "title", selfTokenCount: 50, totalTokenCount: 50 },
        { id: "0:3", name: "subtitle", selfTokenCount: 50, totalTokenCount: 50 },
      ],
    };
    const result = expandedSkeletonToDSL(skeleton);
    expect(result).toContain("header(v:2) #0:1:");
    expect(result).toContain("title #0:2");
    expect(result).toContain("subtitle #0:3");
  });
});

describe("skeletonsToDSL", () => {
  it("should convert multiple skeletons with blank lines", () => {
    const skeletons: CompactNodeSkeleton[] = [
      { i: "0:1", n: "card-1", s: 50, t: 50 },
      { i: "0:2", n: "card-2", s: 50, t: 50 },
    ];
    const result = skeletonsToDSL(skeletons);
    expect(result).toBe("card-1 #0:1\n\ncard-2 #0:2");
  });
});

describe("Complex nested structure with collapse rules", () => {
  it("should produce compact output for real-world component", () => {
    const skeleton: CompactNodeSkeleton = {
      i: "0:100",
      n: "product-card",
      s: 71,
      t: 10500,
      c: [
        {
          i: "0:101",
          n: "Frame 2087327528", // Auto-generated, single child → skip
          s: 33,
          t: 3024,
          c: [
            {
              i: "0:102",
              n: "header",
              s: 20,
              t: 200,
              c: [
                { i: "0:103", n: "ic-star", s: 100, t: 100 }, // Icon
                { i: "0:104", n: "title", s: 50, t: 50 },
              ],
            },
          ],
        },
        {
          i: "0:105",
          n: "features",
          s: 35,
          t: 600,
          c: [
            { i: "0:106", n: "item", s: 100, t: 100 },
            { i: "0:107", n: "item", s: 100, t: 100 },
            { i: "0:108", n: "item", s: 100, t: 100 },
            { i: "0:109", n: "item", s: 100, t: 100 },
          ],
        },
        {
          i: "0:110",
          n: "Button",
          s: 30,
          t: 100,
          c: [{ i: "0:111", n: "label", s: 50, t: 50 }],
        },
      ],
    };

    const result = skeletonToDSL(skeleton);

    expect(result).toContain("product-card(v:7) #0:100:");
    // "Frame 2087327528" is auto-generated, so it's normalized to "container"
    expect(result).toContain("container(v:5) #0:101:");
    expect(result).toContain("header(v:2) #0:102:");
    expect(result).toContain("ic-star #0:103");
    expect(result).toContain("title #0:104");
    expect(result).toContain("features(v:3) #0:105:");
    expect(result).toContain("item #0:106");
    expect(result).toContain("item #0:107");
    expect(result).toContain("item #0:108");
    expect(result).toContain("item #0:109");
    expect(result).toContain("button(v:2) #0:110:");
    expect(result).toContain("label #0:111");
  });
});
