describe("getStructuralSignature", () => {
  it("should create signature for leaf nodes", () => {
    const node: CompactNodeSkeleton = { i: "0:1", n: "title", s: 10, t: 10 };
    expect(getStructuralSignature(node)).toBe("title");
  });

  it("should create signature for nodes with children", () => {
    const node: CompactNodeSkeleton = {
      i: "0:1",
      n: "card",
      s: 50,
      t: 100,
      c: [
        { i: "0:2", n: "title", s: 25, t: 25 },
        { i: "0:3", n: "subtitle", s: 25, t: 25 },
      ],
    };
    expect(getStructuralSignature(node)).toBe("card[subtitle,title]");
  });

  it("should produce identical signatures for structurally identical nodes", () => {
    const node1: CompactNodeSkeleton = {
      i: "0:1",
      n: "row",
      s: 50,
      t: 100,
      c: [
        { i: "0:2", n: "cell", s: 25, t: 25 },
        { i: "0:3", n: "cell", s: 25, t: 25 },
      ],
    };
    const node2: CompactNodeSkeleton = {
      i: "0:10",
      n: "row",
      s: 50,
      t: 100,
      c: [
        { i: "0:11", n: "cell", s: 25, t: 25 },
        { i: "0:12", n: "cell", s: 25, t: 25 },
      ],
    };
    expect(getStructuralSignature(node1)).toBe(getStructuralSignature(node2));
  });
});

