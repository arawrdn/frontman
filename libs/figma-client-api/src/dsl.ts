/**
 * DSL Converter
 * Converts Figma node skeletons to a compact DSL format optimized for AI consumption
 * 
 * Core principle: If expanding a node wouldn't change the implementation approach, don't expand it.
 */

import type { CompactNodeSkeleton, NodeSkeleton } from "./types.js";

// ============================================================================
// Types
// ============================================================================

export interface DSLConversionOptions {
  /** Include source file reference at the top (e.g., @source: filename) */
  sourceFile?: string;
  /** Minimum indentation level (default: 0) */
  baseIndent?: number;
  /** Characters per indent level (default: 2) */
  indentSize?: number;
}

// ============================================================================
// Volume Calculation
// ============================================================================

/**
 * Volume scale based on total token count
 */
export function calculateVolume(totalTokens: number): number {
  if (totalTokens < 100) return 1;
  if (totalTokens < 500) return 2;
  if (totalTokens < 1000) return 3;
  if (totalTokens < 2000) return 4;
  if (totalTokens < 5000) return 5;
  if (totalTokens < 10000) return 6;
  if (totalTokens < 25000) return 7;
  if (totalTokens < 100000) return 8;
  if (totalTokens < 500000) return 9;
  return 10;
}

